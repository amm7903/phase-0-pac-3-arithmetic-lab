//add a and b
function add(a,b){
return a + b;  }

//subtract a and b
function subtract(a,b){
    return a - b;
}

//multiply a and b
function multiply(a,b){
    return a * b;
}

//divide a and b 
function divide(a,b){
    return a / b;
}

//increment up by 1
function increment(a){
    return ++a;
}
//decreases by 1
function decrement(a){
    return --a;
}

//makes int to string
function makeInt(a){
    return a;
}



